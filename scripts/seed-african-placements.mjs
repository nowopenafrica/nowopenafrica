// Seeds the advertisements table with the 58 realistic African placements
// defined in src/data/populateData.ts (single source of truth — parsed from
// that file so the fallback samples and the database never drift apart).
//
// Usage:  node scripts/seed-african-placements.mjs
//
// 1. Deletes ALL existing advertisement rows (they are placeholder demo data)
// 2. Bulk-inserts the 58 African placements
// 3. Always writes scripts/sql/seed_african_ad_placements.sql as a fallback
//    you can paste into the Supabase SQL editor if the API write is blocked
//    by row-level security.

import { readFileSync, writeFileSync } from 'node:fs';
import { dirname, join } from 'node:path';
import { fileURLToPath } from 'node:url';

const here = dirname(fileURLToPath(import.meta.url));
const root = join(here, '..');

// --- load env ---------------------------------------------------------
const env = Object.fromEntries(
  readFileSync(join(root, '.env'), 'utf8')
    .split(/\r?\n/)
    .filter(l => l.includes('='))
    .map(l => [l.slice(0, l.indexOf('=')).trim(), l.slice(l.indexOf('=') + 1).trim()])
);
const URL = env.VITE_SUPABASE_URL;
const KEY = env.VITE_SUPABASE_ANON_KEY;
if (!URL || !KEY) throw new Error('Missing VITE_SUPABASE_URL / VITE_SUPABASE_ANON_KEY in .env');

// --- extract placements from populateData.ts --------------------------
const source = readFileSync(join(root, 'src', 'data', 'populateData.ts'), 'utf8');
const arrMatch = source.match(/const AD_PLACEMENTS: PlacementSeed\[\] = \[([\s\S]*?)\n\];/);
const descMatch = source.match(/const AD_TYPE_DESCRIPTIONS[^{]*\{([\s\S]*?)\n\};/);
if (!arrMatch || !descMatch) throw new Error('Could not find AD_PLACEMENTS in populateData.ts');

// The extracted text is a plain JS array/object literal — evaluate it.
const placements = eval(`[${arrMatch[1]}]`);
const descriptions = eval(`({${descMatch[1]}})`);
console.log(`Parsed ${placements.length} placements from populateData.ts`);

const rows = placements.map(([title, type, location, pricePerDay, dimensions, traffic], i) => ({
  title,
  type,
  category: type,
  description: `${descriptions[type] || 'Premium advertising placement'} — ${dimensions}, ${traffic} traffic. Located at ${location}.`,
  location,
  price_per_day: pricePerDay,
  pricing: pricePerDay,
  budget: pricePerDay * 30,
  duration: 30,
  dimensions,
  traffic_density: traffic,
  status: i % 8 === 0 ? 'pending' : 'active',
  image_url: `https://picsum.photos/seed/placement${i + 1}/400/300.jpg`,
}));

// --- always generate the SQL fallback ---------------------------------
const esc = (v) => (v === null || v === undefined) ? 'NULL'
  : typeof v === 'number' ? String(v)
  : `'${String(v).replace(/'/g, "''")}'`;

const cols = ['title', 'type', 'category', 'description', 'location', 'price_per_day', 'pricing', 'budget', 'duration', 'dimensions', 'traffic_density', 'status', 'image_url'];
const sql = [
  '-- Replaces ALL advertisement rows with 58 realistic African placements.',
  '-- Generated by scripts/seed-african-placements.mjs — do not edit by hand.',
  '-- DESTRUCTIVE: deletes existing advertisements first.',
  '',
  'DELETE FROM advertisements;',
  '',
  `INSERT INTO advertisements (${cols.join(', ')}) VALUES`,
  rows.map(r => `  (${cols.map(c => esc(r[c])).join(', ')})`).join(',\n') + ';',
  '',
].join('\n');
const sqlPath = join(root, 'scripts', 'sql', 'seed_african_ad_placements.sql');
writeFileSync(sqlPath, sql, 'utf8');
console.log(`Wrote SQL fallback: ${sqlPath}`);

// --- try to apply live via the REST API -------------------------------
// Anonymous writes are (correctly) blocked by RLS, so authenticate first.
// Uses SEED_EMAIL / SEED_PASSWORD env vars if provided, otherwise signs up
// a dedicated seeder account.
let accessToken = KEY;
let seederId = null;

const auth = async (path, body) => {
  const res = await fetch(`${URL}/auth/v1/${path}`, {
    method: 'POST',
    headers: { apikey: KEY, 'Content-Type': 'application/json' },
    body: JSON.stringify(body),
  });
  return { ok: res.ok, data: await res.json() };
};

const email = process.env.SEED_EMAIL || 'seeder@nowopen.africa';
const password = process.env.SEED_PASSWORD || 'NowOpen-Seeder-2026!';

let session = await auth('token?grant_type=password', { email, password });
if (!session.ok) {
  console.log('Sign-in failed, trying to create the seeder account...');
  const signup = await auth('signup', { email, password });
  if (signup.ok && signup.data.access_token) {
    session = { ok: true, data: signup.data };
  } else if (signup.ok && !signup.data.access_token) {
    console.error('Signup succeeded but email confirmation is required — no session available.');
    console.error('Either disable email confirmation temporarily, or run the generated SQL file.');
    process.exit(1);
  } else {
    console.error(`Auth failed: ${JSON.stringify(signup.data)}`);
    console.error('Run the generated SQL file in the Supabase SQL editor instead.');
    process.exit(1);
  }
}
accessToken = session.data.access_token;
seederId = session.data.user?.id;
console.log(`Authenticated as ${email}`);
rows.forEach(r => { r.user_id = seederId; });

const headers = {
  apikey: KEY,
  Authorization: `Bearer ${accessToken}`,
  'Content-Type': 'application/json',
};

const del = await fetch(`${URL}/rest/v1/advertisements?id=not.is.null`, {
  method: 'DELETE',
  headers: { ...headers, Prefer: 'count=exact' },
});
if (!del.ok) {
  console.error(`DELETE blocked (${del.status}): ${await del.text()}`);
  console.error('\nRow-level security prevents anonymous writes (this is good!).');
  console.error('Run the generated SQL file in the Supabase SQL editor instead.');
  process.exit(1);
}
console.log(`Deleted existing rows (${del.headers.get('content-range') || 'ok'})`);

const ins = await fetch(`${URL}/rest/v1/advertisements`, {
  method: 'POST',
  headers: { ...headers, Prefer: 'return=minimal' },
  body: JSON.stringify(rows),
});
if (!ins.ok) {
  console.error(`INSERT failed (${ins.status}): ${await ins.text()}`);
  console.error('Run the generated SQL file in the Supabase SQL editor instead.');
  process.exit(1);
}
console.log(`Inserted ${rows.length} African placements. Done!`);
